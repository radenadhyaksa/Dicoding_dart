void main() {
  /**
   * ==	Sama dengan
   * !=	Tidak sama dengan
   * >	Lebih dari
   * <	Kurang dari
   * >=	Lebih dari sama dengan
   * <=	Kurang dari sama dengan */

  if (2 <= 3) {
    print('Ya, 2 kurang dari sama dengan 3');
  } else {
    print('Anda salah');
  }

/*
Output:
  Ya, 2 kurang dari sama dengan 3
 */
}
